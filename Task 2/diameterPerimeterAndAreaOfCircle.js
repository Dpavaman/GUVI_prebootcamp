//Given their radius of a circle and find its diameter, circumference and area.
var radius = 3;
var area = Math.PI*radius*radius;
var perimeter = 2*Math.PI*radius
var diameter = 2*radius;

console.log("area :",area.toFixed(2)); //28.27
console.log("perimeter :",perimeter.toFixed(2)); // 18.85
console.log("diameter :",diameter.toFixed(2)); // 6.00
