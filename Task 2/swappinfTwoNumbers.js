//Swapping 2 numbers 

var num1 = 35;
var num2 = 99;
//BEFORE SWAPPING
console.log("Number1 :",num1 + " and Number2 :",num2); //Number1 = 35, Number2 = 99
var temp = num1;

num1 = num2;
num2 = temp;
//AFTER SWAPPING
console.log("Number1 :",num1 + " and Number2 :",num2); //Number1 = 99, Number2 = 35

